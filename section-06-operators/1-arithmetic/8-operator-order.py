# Orden de ejecución de operaciones aritméticas

a = (3+1) + 4**2 * 2 # 36 => () => ** => * => +

a = 4 * 5 + 6 - 3    # 23 => * => + => -

a = 4 * 5 * 6 - 3    # 117 => * => * => -

a = 4 * 5 * (6 - 3)   # 60 => (-) => * => *

a = 9 / (3 * 2 + 3)     # 1 (* => +) => /
a = 9 / 3 * 2 + 3    # 9 / => * => +

